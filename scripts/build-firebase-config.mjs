/**
 * .env 또는 환경 변수로부터 js/firebase-config.generated.js 를 생성합니다.
 * 로컬: .env 를 두고 `npm run build:config`
 * CI:  FIREBASE_* 환경 변수를 설정한 뒤 이 스크립트 실행
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outPath = path.join(root, "js", "firebase-config.generated.js");
const envPath = path.join(root, ".env");

const KEYS = [
  "FIREBASE_API_KEY",
  "FIREBASE_AUTH_DOMAIN",
  "FIREBASE_PROJECT_ID",
  "FIREBASE_STORAGE_BUCKET",
  "FIREBASE_MESSAGING_SENDER_ID",
  "FIREBASE_APP_ID",
];

function loadEnvFile() {
  if (!fs.existsSync(envPath)) {
    return {};
  }
  const out = {};
  for (const line of fs.readFileSync(envPath, "utf8").split("\n")) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    const i = t.indexOf("=");
    if (i === -1) continue;
    const k = t.slice(0, i).trim();
    let v = t.slice(i + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    out[k] = v;
  }
  return out;
}

function resolveEnv(fileEnv) {
  const merged = {};
  for (const k of KEYS) {
    const v = process.env[k] ?? fileEnv[k];
    if (v != null && String(v).length > 0) {
      merged[k] = v;
    }
  }
  return merged;
}

function buildConfig(e) {
  return {
    apiKey: e.FIREBASE_API_KEY,
    authDomain: e.FIREBASE_AUTH_DOMAIN,
    projectId: e.FIREBASE_PROJECT_ID,
    storageBucket: e.FIREBASE_STORAGE_BUCKET,
    messagingSenderId: e.FIREBASE_MESSAGING_SENDER_ID,
    appId: e.FIREBASE_APP_ID,
  };
}

const fileEnv = loadEnvFile();
const resolved = resolveEnv(fileEnv);
const missing = KEYS.filter((k) => !resolved[k]);

if (missing.length) {
  console.error(
    "누락된 값:",
    missing.join(", "),
    "\n\n.env 를 생성하려면: .env.example 을 .env 로 복사한 뒤 값을 입력하세요.",
    "\n또는 CI 에서는 FIREBASE_* 환경 변수를 설정하세요."
  );
  process.exit(1);
}

const cfg = buildConfig(resolved);
const body = `// AUTO-GENERATED by scripts/build-firebase-config.mjs — DO NOT COMMIT
export const FIREBASE_CONFIG = ${JSON.stringify(cfg, null, 2)};
`;

fs.writeFileSync(outPath, body, "utf8");
console.log("작성:", outPath);
